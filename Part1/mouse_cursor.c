/* ── Character buffer helpers ─────────────────────────────────────────── */

/*
 * Write a single character at character-grid position (cx, cy).
 * cx : 0-79, cy : 0-59
 */
void char_buf_put(int cx, int cy, char ch)
{
    volatile char *char_buf = (volatile char *)CHAR_BUF_BASE;
    int offset = (cy << 7) + cx;   /* cy*128 + cx  (128 = 2^7 stride) */
    *(char_buf + offset) = ch;
}

/*
 * Write a null-terminated string starting at character position (cx, cy).
 * Does NOT wrap; caller must ensure it fits on one line.
 */
void char_buf_puts(int cx, int cy, const char *s)
{
    volatile char *char_buf = (volatile char *)CHAR_BUF_BASE;
    int offset = (cy << 7) + cx;
    while (*s) {
        *(char_buf + offset) = *s;
        ++s;
        ++offset;
    }
}

/*
 * Clear the entire character buffer (fill with spaces so no stale text).
 */
void char_buf_clear(void)
{
    volatile char *char_buf = (volatile char *)CHAR_BUF_BASE;
    int row, col;
    for (row = 0; row < CHAR_BUF_ROWS; row++)
        for (col = 0; col < CHAR_BUF_COLS; col++)
            *(char_buf + (row << 7) + col) = ' ';
}

/* ── Low-level pixel ──────────────────────────────────────────────────── */
void plot_pixel(int x, int y, short int c)
{
    if (x < 0 || x >= FB_WIDTH)  return;
    if (y < 0 || y >= FB_HEIGHT) return;
    *(volatile short int *)(pixel_buffer_start + (y << 10) + (x << 1)) = c;
}

void fill_rect(int x, int y, int w, int h, short int c)
{
    int px, py;
    for (py = y; py < y + h; py++)
        for (px = x; px < x + w; px++)
            plot_pixel(px, py, c);
}

/* ── Staff-line query ─────────────────────────────────────────────────── */
int is_staff_line(int x, int y)
{
    int s, l;
    if (x < STAFF_LEFT || x > STAFF_RIGHT) return 0;
    for (s = 0; s < NUM_STAFFS; s++)
        for (l = 0; l < NUM_LINES; l++)
            if (y == staff_top[s] + l * LINE_GAP) return 1;
    return 0;
}

/* ── Background colour at (x,y) ───────────────────────────────────────── */
short int bg_color(int x, int y)
{
    if (y >= MENUBAR_H && is_staff_line(x, y)) return BLACK;
    return WHITE;
}

/* ── restore_pixel: dot > menubar > staff line > white ───────────────── */
void restore_pixel(int x, int y)
{
    int i, ddx, ddy;
    for (i = 0; i < num_dots; i++) {
        ddx = x - dot_x[i];
        ddy = y - dot_y[i];
        if (ddx*ddx + ddy*ddy <= DOT_R*DOT_R) {
            plot_pixel(x, y, BLACK);
            return;
        }
    }
    if (y < MENUBAR_H) {
        plot_pixel(x, y, DARK_GREY);
        return;
    }
    plot_pixel(x, y, bg_color(x, y));
}

