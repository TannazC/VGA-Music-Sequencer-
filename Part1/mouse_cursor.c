void draw_treble_clef(int ox, int oy)
{
    //render_w define the size width to show on screen
    int render_w = 28; // Restored perfect aspect ratio width
    int render_h = 56;
    int row, col, src_y, src_x;
    int byte_idx, bit_idx;

    for (row = 0; row < render_h; row++) {
        src_y = 10 + (row * 93) / render_h;

        for (col = 0; col < render_w; col++) {
            int bitmap_width = 44; // Original bitmap width
            int bitmap_height = 38; // Original bitmap height
            
            src_x = 38 + (col * 44) / render_w;

            byte_idx = (src_y * SHEET_WIDTH_BYTES) + (src_x / 8);
            bit_idx  = 7 - (src_x % 8);

            // This ONLY draws black pixels. White pixels are completely ignored (transparent!)
            if ((sheet_music_bitmap[byte_idx] >> bit_idx) & 1) {
                plot_pixel(ox + col, oy + row, BLACK); // Fixed the backward flip
            }
        }
    }
}

