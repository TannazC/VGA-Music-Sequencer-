/* ═══════════════════════════════════════════════════════════════════════
   Screen-state globals
   ═══════════════════════════════════════════════════════════════════════ */

/**
 * @brief Non-zero while any start or song-select screen is being displayed.
 *
 * Checked by the main loop to suppress sequencer input handling while the
 * boot screens are active.
 */
extern volatile int g_start_screen_active;

/**
 * @brief Currently highlighted option on the main start screen.
 *
 * 1 = Create Your Own, 2 = Preload Song.
 */
extern int g_start_selection;

/**
 * @brief Currently highlighted song on the song-select screen.
 *
 * 1 = Do Re Mi, 2 = Fur Elise, 3 = Ode to Joy, 4 = Seven Nation Army.
 */
extern int g_song_selection;

/* ═══════════════════════════════════════════════════════════════════════
   Public functions
   ═══════════════════════════════════════════════════════════════════════ */

/**
 * @brief Renders the full main start screen.
 *
 * Clears the display, draws decorative staves, the title logo, and the
 * initial menu buttons. Resets g_start_selection to 1.
 */
void draw_start_screen(void);

/**
 * @brief Repaints only the two main-menu buttons for the given selection.
 *
 * Call whenever g_start_selection changes to avoid redrawing the full screen.
 *
 * @param active_opt Highlighted option (1 = Create Your Own, 2 = Preload Song).
 */
void update_start_selection(int active_opt);

/**
 * @brief Renders the full song-select screen.
 *
 * Clears the display, draws a decorative staff, the title logo, a section
 * label, and all song option buttons. Resets g_song_selection to 1.
 */
void draw_song_select_screen(void);

/**
 * @brief Repaints only the song option buttons for the given selection.
 *
 * Call whenever g_song_selection changes to avoid redrawing the full screen.
 *
 * @param active_opt Highlighted song (1–4). Option 5 (Back) is always inactive.
 */
void update_song_selection(int active_opt);

#endif /* START_SCREEN_H */
